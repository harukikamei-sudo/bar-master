// 時間帯別の挨拶
export function getGreeting() {
  const h = new Date().getHours();
  if (h >= 6 && h < 15) return 'こんにちは。昼から一杯、いいですね。\n今日はどんな気分ですか？';
  if (h >= 15 && h < 18) return 'お疲れさまです。今日も一日頑張りましたね。\n一杯いかがですか？';
  if (h >= 18 && h < 24) return 'こんばんは。いい夜ですね。\n何かお探しでしたら、お気軽にどうぞ。';
  return '遅い時間までお疲れさまです。\n夜更かしのお供はいかがですか？';
}

// 再訪挨拶
export function getReturnGreeting(lastDrink) {
  const greetings = [
    'おかえりなさい。またお会いできましたね。今日は何にしましょうか？',
    'お久しぶりです。新しいのが入りましたよ。見ていかれますか？',
  ];
  if (lastDrink) {
    greetings.unshift(`おかえりなさい。前回の${lastDrink}、いかがでしたか？`);
  }
  return greetings[Math.floor(Math.random() * greetings.length)];
}

// 訪問回数メッセージ
export function getVisitMessage(count) {
  if (count === 3) return 'もう常連さんですね。いつもありがとうございます。';
  if (count === 5) return 'いらしてくれると、こっちも嬉しくなります。';
  if (count >= 10) return 'もうすっかり馴染みですね。今日は特別にいいものをご用意してますよ。';
  return null;
}

